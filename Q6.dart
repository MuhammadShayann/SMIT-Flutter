// Q.6: Write a program to check whether an alphabet is a vowel or consonant.
void main() {
  String Alphabets = 'A';

  if (Alphabets == 'a' ||
      Alphabets == 'e' ||
      Alphabets == 'i' ||
      Alphabets == 'o' ||
      Alphabets == 'u') {
    print("It is Vowel");
  } else {
    print("It is Consonant");
  }
}
