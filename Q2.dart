// Q.2: Take two variables and store age then using if/else condition to determine oldest and youngest among them.

void main() {
  int age1 = 17;
  int age2 = 20;

  if (age1 > age2) {
    print("oldest");
  } else if (age1 < age2) {
    print("Youngest");
  } else {
    print("Both are same age");
  }
}
